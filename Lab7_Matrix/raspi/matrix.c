void write_byte(unsigned char data)
{
	unsigned char i;
	digitalWrite(PIN_CS, LOW);
	for (i = 8; i >= 1; i--)
	{
		digitalWrite(PIN_CLK, LOW);
		digitalWrite(PIN_DIN, data&0x80);
		data = data << 1;
		digitalWrite(PIN_CLK, HIGH);
	}
}

void write(unsigned char addr, unsigned char data)
{
	digitalWrite(PIN_CS, LOW);
	write_byte(addr);
	write_byte(data);
	digitalWrite(PIN_CS, HIGH);
}

void init()
{
	write(0x09, 0x00);
	write(0x0a, 0x03);
	write(0x0b, 0x07);
	write(0x0c, 0x01);
	write(0x0f, 0x00);
}

